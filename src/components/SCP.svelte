			**Item #:** SCP-XXXX  
      **Object Class:** Euclid  
    </p>
    <p>
			**Special Containment Procedures:**  
      SCP-XXXX is to be kept in a standard containment chamber at Site-██. Access to SCP-XXXX is restricted to Level 3 personnel and higher. Any experimentation with SCP-XXXX must be approved by no less than two Level 4 researchers. All instances of SCP-XXXX-1 events must be reported immediately. Personnel are prohibited from interacting with SCP-XXXX for more than 30 minutes at a time. The containment chamber must remain under continuous observation via remote surveillance.
    </p>
    <p>
			**Description:**  
      SCP-XXXX is a vintage rotary telephone, black in color, with no discernible markings indicating its manufacturer. Despite its dated appearance, SCP-XXXX displays no signs of wear or degradation and remains in pristine condition. SCP-XXXX is fully functional and can be connected to standard telephone lines, although this is not required for anomalous activity to occur.
    </p>
    <p>
			When SCP-XXXX is left inactive for prolonged periods (typically 24-72 hours), it will begin to ring. Answering SCP-XXXX triggers an event designated as SCP-XXXX-1. During an SCP-XXXX-1 event, the individual who answers the phone (hereafter referred to as the "subject") will begin a conversation with an unknown entity, which refers to itself as "The Operator."
    </p>
    <p>
			"The Operator" speaks in a calm, monotone voice, and its conversation initially appears innocuous, consisting of standard pleasantries or trivial questions (e.g., "How was your day?" or "What is the weather like?"). However, the longer the subject remains engaged in conversation, the more disturbing the dialogue becomes. Topics eventually shift to personal fears, regrets, and traumas, often referencing highly specific incidents from the subject's past that have not been publicly disclosed. 
    </p>
    <p>
			Subjects report feeling increasingly compelled to continue speaking with "The Operator," often against their own will. The conversation continues until either SCP-XXXX is forcibly disconnected, or the subject terminates the call. However, subjects who successfully disconnect the call report auditory hallucinations, such as faint ringing or whispers, for several hours after the event. 
    </p>
    <p>
			Prolonged exposure to SCP-XXXX-1 events has been shown to result in lasting psychological effects, including heightened anxiety, insomnia, and in some cases, the development of paranoid delusions. Subjects have described a growing belief that "The Operator" continues to observe them even after the call has ended.
    </p>
    <p>
			**Addendum XXXX-A:**  
      On ██/██/20██, during Experiment XXXX-17, Dr. █████, who had engaged in multiple SCP-XXXX-1 events, received a personal phone call on his mobile device from an unknown number. Upon answering, Dr. █████ immediately became unresponsive, staring blankly at his surroundings. Security personnel were dispatched to Dr. █████'s location and found him still holding the phone, which was emitting the same calm, monotone voice identified as "The Operator." Dr. █████ was subsequently placed under observation, and SCP-XXXX was isolated for further study.  
    </p>
    <p>
			**Addendum XXXX-B:**  
      As of ██/██/20██, SCP-XXXX has begun exhibiting a new phenomenon. Individuals within close proximity to SCP-XXXX report hearing faint whispers emanating from the device, even when it is not connected to a phone line. These whispers are reported to be indistinct, but subjects consistently describe them as "pleading" or "begging" in tone. Further investigation is ongoing.
    </p>
    <p>
			**Note:** SCP-XXXX's true purpose and the origin of "The Operator" remain unknown. Researchers are currently exploring possible connections to known reality-altering entities or telepathic anomalies. All personnel are advised to limit exposure to SCP-XXXX to prevent long-term psychological damage.
